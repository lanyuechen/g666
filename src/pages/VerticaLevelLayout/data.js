const parseData = (data) => {
  const nodes = [];
  const edges = [];
  const combos = {};

  let lastSwitch = '';
  data.forEach(([host, nc, port, sw, port1, sw1, port0, sw0]) => {
    lastSwitch = sw || lastSwitch;

    if (sw0) {
      nodes.push({
        id: `${sw0}-${port0}`,
        label: port0.split('/').pop(),
        originLabel: port0,
        comboId: sw0,
        style: {
          fill: 'orange',
          stroke: 'orangered',
        },
        stateStyles: {
          highlight: {
            fill: 'orange',
            stroke: 'black',
          },
        },
      }, {
        id: `${sw1}-${port1}`,
        label: port1.split('/').pop(),
        originLabel: port1,
        comboId: sw1,
        style: {
          fill: 'orange',
          stroke: 'orangered',
        },
        stateStyles: {
          highlight: {
            fill: 'orange',
            stroke: 'black',
          },
        },
      });
      combos[sw0] = {
        id: sw0,
        level: 0,
        label: sw0,
        padding: [30, 20, 20, 20],
      }
      edges.push({
        source: `${sw0}-${port0}`,
        target: `${sw1}-${port1}`,
        targetAnchor: 0,
      });
      return;
    } else if (sw1) {
      nodes.push({
        id: `${sw1}-${port1}`,
        label: port1.split('/').pop(),
        originLabel: port1,
        comboId: sw1,
        style: {
          fill: 'lightgreen',
          stroke: 'green',
        },
        stateStyles: {
          highlight: {
            fill: 'lightgreen',
            stroke: 'black',
          },
        },
      },
      {
        id: `${lastSwitch}-${port}`,
        label: port.split('/').pop(),
        originLabel: port,
        comboId: lastSwitch,
        style: {
          fill: 'lightgreen',
          stroke: 'green',
        },
        stateStyles: {
          highlight: {
            fill: 'lightgreen',
            stroke: 'black',
          },
        },
      });
      combos[sw1] = {
        id: sw1,
        level: 1,
        label: sw1,
        padding: [30, 20, 20, 20],
      }
      edges.push({
        source: `${sw1}-${port1}`,
        target: `${lastSwitch}-${port}`,
        targetAnchor: 0,
      });
      return;
    }

    nodes.push({
      id: `${host}-${nc}`,
      label: nc.match(/eth(\d+)/i)[1] || nc,
      originLabel: nc,
      comboId: host,
    });

    nodes.push({
      id: `${lastSwitch}-${port}`,
      label: port.split('/').pop(),
      originLabel: port,
      comboId: lastSwitch,
    });

    edges.push({
      source: `${lastSwitch}-${port}`,
      target: `${host}-${nc}`,
      targetAnchor: 0,
    });

    combos[lastSwitch] = {
      id: lastSwitch,
      level: 2,
      label: lastSwitch,
      padding: [30, 20, 20, 20],
    }

    combos[host] = {
      id: host,
      level: 3,
      label: host,
      padding: [30, 20, 20, 20],
    }
  });

  const sourceAnchorLeft = [];
  Object.values(combos).forEach(combo => {
    const fNodes = nodes.filter(d => d.comboId === combo.id);
    if (fNodes.length > 11) {
      for (let i = 0; i < 11; i++) {
        sourceAnchorLeft.push(fNodes[i].id)
      }
    }
  });

  edges.forEach(edge => {
    if (sourceAnchorLeft.includes(edge.source)) {
      edge.type = 'sw-line';
      edge.sourceAnchor = 3;
    }
    edge.targetAnchor = 0;
  })

 return { nodes, edges, combos: Object.values(combos) };
}

export default parseData(data);